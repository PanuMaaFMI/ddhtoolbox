program gen
!
!-------------------------------------------------
! Types implicites.
!-------------------------------------------------
!
#include"/home/piriou/ftn/implicit_r8i4.h"
!
!-------------------------------------------------
! On va tracer ic courbes.
!-------------------------------------------------
!
ic=24
zal=0.2541251
do jc=1,ic
  !
  !-------------------------------------------------
  ! Chaque courbe comporte ix points.
  !-------------------------------------------------
  !
  ix=60
  !
  !-------------------------------------------------
  ! Fichier de sortie.
  !-------------------------------------------------
  !
  write(clfic,fmt='(a,i2.2,a)') 'evol.',jc,'.dta'
  iul=40 ; open(iul,file=clfic,form='formatted')
  !
  !-------------------------------------------------
  ! Chaque courbe démarre de y=0.
  !-------------------------------------------------
  !
  zy=0.
  do jx=1,ix
    write(iul,fmt=*) jx,zy
    !
    !-------------------------------------------------
    ! Suite récurrente de y: calcul du y suivant.
    !-------------------------------------------------
    !
    zy=zy+zal-0.5
    !
    !-------------------------------------------------
    ! On renouvelle le nombre aléatoire zal.
    !-------------------------------------------------
    !
    zal=9821.*zal+0.211327
    zal=zal-int(zal)
  enddo
  close(iul)
enddo
end
